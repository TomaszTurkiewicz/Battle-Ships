package com.example.ships.classes;

public class PointIJ {
   private int i;
   private int j;

    public PointIJ(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public PointIJ() {
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }
}
